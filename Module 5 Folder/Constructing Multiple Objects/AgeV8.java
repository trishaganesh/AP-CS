public class AgeV8 {

    private String name;
    private int age;

    // one parameter constructor
    public AgeV8(String name){
        this.name = name;
        this.age = 0;
    }
    // two parameter constructor
    public AgeV8(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public double calculateAverageAge(int ageNow, int ageTwoYearsAgo) {
        return ((double)ageNow + ageTwoYearsAgo) / 2;
    }

    public double calculateAverageAge(int ageTwoYearsAgo) {
        double average = (this.age + (double)ageTwoYearsAgo)/2.0;
        return average;
    }
}
